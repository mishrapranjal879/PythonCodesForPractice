# Logical operators----
# and (in and operator left and right both conditions are true then final result will be true, in simple words we can say , all condition is true then final result will be true if any one condition is false then final result will be false)
print(12==12 and 56 != 45)
# or---(if only one condition is true then final result will be also true)

print(12 == 12 or 45 == 56 or 23 == 23)

# not---- (it is covert true condition in false and false condition in true)

print(not 12 == 12)
